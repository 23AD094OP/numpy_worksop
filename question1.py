# write a python code to find if the given number is odd or even?
num = int(input("Enter the number:- "))
if num%2 == 0:
    print(num," is a EVEN Number")
else:
    print(num," is a ODD Number") 
# output:-     
# Enter the number:- 5
# 5  is a ODD Number   
# Enter the number:- 6
# 6  is a EVEN Number
